CREATE TABLE Scenario (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid UUID NOT NULL,
    name TEXT NOT NULL,
    text TEXT NOT NULL,
    clientId BIGINT,
    friendIds BIGINT[],
    firstTimeToActivate TIMESTAMP,
    firstTimeToActivateOrigin TIMESTAMP,
    allowedDelayAfterPing INT,
    okFromAntispam BOOLEAN,
    okByHand BOOLEAN,
    textToPing TEXT
);
